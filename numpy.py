import numpy as np

# A Python list
py_list = [1, 2, 3, 4]

# A NumPy array
np_array = np.array([1, 2, 3, 4])

print(f"Python List: {py_list}")
print(f"NumPy Array: {np_array}")

# Example of addition
print("Python List addition:", [1,2] + [3,4])
print("NumPy Array addition:", np.array([1,2]) + np.array([3,4]))
